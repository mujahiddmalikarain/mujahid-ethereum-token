let signer;
let tokenContract;

document.getElementById("connectWallet").addEventListener("click", async () => {
  if (window.ethereum) {
    const provider = new ethers.BrowserProvider(window.ethereum);
    await window.ethereum.request({ method: "eth_requestAccounts" });
    signer = await provider.getSigner();

    tokenContract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer);

    const walletAddress = await signer.getAddress();
    document.getElementById("walletAddress").innerText = walletAddress;
    document.getElementById("status").innerText = "Wallet connected!";
  } else {
    alert("Please install MetaMask to use this feature.");
  }
});

document.getElementById("checkBalance").addEventListener("click", async () => {
  if (tokenContract) {
    const balance = await tokenContract.balanceOf(await signer.getAddress());
    console.log(balance);
    document.getElementById("balance").innerText = ethers.formatUnits(balance, 18);
  } else {
    alert("Connect wallet first.");
  }
});

document.getElementById("transfer").addEventListener("click", async () => {
  const recipient = document.getElementById("recipient").value;
  const amount = document.getElementById("amount").value;

  if (tokenContract && recipient && amount) {
    try {
      const tx = await tokenContract.transfer(recipient, ethers.parseUnits(amount, 18));
      await tx.wait();
      document.getElementById("status").innerText = `Transfer successful! Tx Hash: ${tx.hash}`;
    } catch (error) {
      document.getElementById("status").innerText = "Transfer failed. " + error.message;
    }
  } else {
    alert("Enter recipient address and amount.");
  }
});
